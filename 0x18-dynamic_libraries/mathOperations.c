#include <stdio.h>

/**
 * add - add two int
 * @a: first int
 * @b: second int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two int
 * @a: first int
 * @b: second int
 * Return: final subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two int
 * @a: first int
 * @b: second int
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two int
 * @a: first int
 * @b: second int
 * Return: ans
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulate two int
 * @a: first int
 * @b: second int
 * Return: modulation
 */
int mod(int a, int b)
{
	return (a % b);
}
